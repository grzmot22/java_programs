/** RelationalOperators1.java
	Use of <, >, >= and <=
	Des, 18th October 2000
*/
public class RelationalOperators1 
{
	public static void main(String [] args) 
	{
		int x = 12;
		int y = 24;
		boolean z = false;
		z = x > y;
		System.out.println("z = " + z);	// Predict output
		z = x < y;
		System.out.println("z = " + z);
	}
}
